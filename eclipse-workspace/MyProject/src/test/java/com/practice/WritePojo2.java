package com.practice;

public class WritePojo2 {
	private String subName;
	private int mark;
	public WritePojo2(String subName, int mark) {
		super();
		this.subName = subName;
		this.mark = mark;
	}
	public String getSubName() {
		return subName;
	}
	public void setSubName(String subName) {
		this.subName = subName;
	}
	public int getMark() {
		return mark;
	}
	public void setMark(int mark) {
		this.mark = mark;
	}
	
	
	
	
}
